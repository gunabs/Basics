package com.basic.pkg;

public class Forloops {

	public static void main(String[] args) {
		for (int no=1;no<=6;no++)
		{
			System.out.println(no);
		}
		{
		System.out.println("hello");
		}
		
		int no=1;
		
	for (;no<=6;no++)
	{
		System.out.println(no);
	}
	System.out.println("hello"+no);

	}

}
